#!/bin/bash

--title_screen() {

    echo "================================================================================================================"
    echo "================================================================================================================"
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "==================                          THE LEGEND OF ZELDA                               =================="
    echo "==================                             HYRULE CASTLE                                  =================="
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "==================                           1- NEW GAME                                      =================="
    echo "==================                           2- QUIT                                          =================="
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "================================================================================================================"
    echo "================================================================================================================"

    cpt=0
    while [[ $cpt = 0 ]]; do
	read -s -n 1 choice
	if [[ $choice = 1 || $choice = 2 ]]; then
	    want_play=$choice
	    cpt=1
	else
	    echo "Chose between 1 (New game) or 2 (Quit)"
	fi
    done
    
}

--difficulty_selection() {
   
    echo "================================================================================================================"
    echo "================================================================================================================"
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "==================                          THE LEGEND OF ZELDA                               =================="
    echo "==================                          Chose your difficulty                             =================="
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "==================                           1- Normal                                        =================="
    echo "==================                           2- Difficult                                     =================="
    echo "==================                           3- Insane                                        =================="
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "================================================================================================================"
    echo "================================================================================================================"

 
    read -s -n 1 choice
    if [[ $choice = 1 ]]; then
	dif=$choice
	echo "You set the difficulty to Normal"
    elif [[ $choice = 2 ]]; then
	dif=$choice
	echo "You set the difficulty to Difficult"
    elif [[ $choice = 3 ]]; then
	dif=$choice
	echo "You set the difficulty to Insane"
    else
	echo "If you can't even input a right number, we will set the difficulty on easy for you."
	echo "Every monster's stat will be divided by 2."
	echo "Have fun, noob!"
	dif=0
    fi
    
}

--fight_nb_selection() {

    echo "================================================================================================================"
    echo "================================================================================================================"
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "==================                          THE LEGEND OF ZELDA                               =================="
    echo "==================                          Chose the number of floor                         =================="
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "==================                           1- 10                                            =================="
    echo "==================                           2- 20                                            =================="
    echo "==================                           3- 50                                            =================="
    echo "==================                           4- 100                                           =================="
    echo "==================                                                                            =================="
    echo "==================                                                                            =================="
    echo "================================================================================================================"
    echo "================================================================================================================"

    cpt=0
    while [[ $cpt = 0 ]]; do
	read -s -n 1 choice
	if [[ $choice = 1 || $choice = 2 || $choice = 3 || $choice = 4 ]]; then
	    if [[ $choice = 1 ]]; then
		fight_nb=1
	    elif [[ $choice = 2 ]]; then
		fight_nb=2
	    elif [[ $choice = 3 ]]; then
		fight_nb=5
	    elif [[ $choice = 4 ]]; then
		fight_nb=10
	    fi
	    cpt=1
	    echo "You chose to play with $(($fight_nb * 10)) floors"
	else
	    echo "Chose between 1 (10 floors), 2 (20 floors), 3 (50 floors) or 4 (100 floors)"
	fi
	    done
}
